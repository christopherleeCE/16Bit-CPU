INIT:
    MOV G3 8082
    MOV G7 8099
    MOV G6 80A4
    JMP START:

DRAW_CIRCLE:
    SUBI G1 G0 0001
    SW G1 G4 0000
    ADDI G1 G0 0001
    SW G1 G4 0000
    SUBI G1 G0 0010
    SW G1 G4 0000
    ADDI G1 G0 0010
    SW G1 G4 0000    
    JR LR

START:
    ADD G0 G3 0000
    ADDI G4 ZERO 0002
    JAL DRAW_CIRCLE: LR

    ADD G0 G7 0000
    ADDI G4 ZERO 0003
    JAL DRAW_CIRCLE: LR

    ADD G0 G6 0000
    ADDI G4 ZERO 0004
    JAL DRAW_CIRCLE: LR

    MOV G0 FFFF
    JMP END:

END: